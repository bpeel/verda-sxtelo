import sys

HEADER="""\
/* Automatically generated by make-crc-table.py */

static const uint32_t
crc_table[256] = {\
"""

FOOTER="""\
};\
"""

with open(sys.argv[1], "w", encoding="utf-8") as out:
    print(HEADER, file = out)

    for n in range(256):
        c = n

        for bit in range(8):
            value = c & 1

            c >>= 1

            if value != 0:
                c ^= 0xedb88320

        print(f"        0x{c & 0xffffffff:08x},", file = out)

    print(FOOTER, file = out)
